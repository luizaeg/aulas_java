package java_aula_3;

public class OperadoresRelacionais {

	public static void main(String[] args) {

	// Operadores Relacionais
		
		int ab = 10;
		int cd = 5;
		int ef = 5;
		
				
		System.out.printf("\n %d > %d = %b", ab, cd, (ab > cd));
		System.out.printf("\n %d > %d = %b", ab, cd, (ab < cd));
		System.out.printf("\n %d > %d = %b", cd, ef, (cd = ef));
		
	}

}
